def hello_world(name):
    """Greet the user by name."""
    print(f"Hello, {name}!")

def calculate_sum(a, b):
    """Calculate the sum of two numbers."""
    return a + b

# Using hello_world function
hello_world("Alice")
hello_world("Bob")

# Using calculate_sum function
result = calculate_sum(5, 10)
print(f"Sum: {result}") 